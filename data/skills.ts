// 辅助查找函数
export function getSkillById(id: string): Skill | undefined {
  return skills.find(s => s.id === id);
}

export function getSkillsByCategory(category: string): Skill[] {
  return skills.filter(s => s.category === category);
}

export function getPrerequisiteSkills(skillId: string): Skill[] {
  const skill = getSkillById(skillId);
  if (!skill) return [];
  return skill.prerequisites.map(pid => getSkillById(pid)).filter(Boolean) as Skill[];
}

export function getUnlockableSkills(skillId: string): Skill[] {
  return skills.filter(s => s.prerequisites.includes(skillId));
}
