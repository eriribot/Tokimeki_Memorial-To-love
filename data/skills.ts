export interface Skill {
  id: string;
  name: string; // 中文名称
  originalName: string; // 日文原名
  category: '运动' | '沟通' | '学问' | '课外活动' | '其他' | '上位';
  cost: number; // SP经验消耗
  description: string; // 技能效果说明
  rating: string; // 推荐度评级
  prerequisites: string[]; // 前置技能ID
  triggerCondition?: string; // 发动条件
  triggerRate?: string; // 发动概率
  effects: SkillEffect[];
  isAdvanced?: boolean; // 是否为上位技能
  isLicense?: boolean; // 是否为执照
}

export interface SkillEffect {
  type: 'stat_boost' | 'stat_reduce' | 'success_rate' | 'trigger_rate' | 'heal' | 'moral' | 'special' | 'command_seal' | 'damage_reduce' | 'exp_boost';
  target?: string;
  value?: string;
  description: string;
}

export interface SkillCombo {
  name: string;
  description: string;
  skills: string[]; // 技能ID列表
  totalCost: number;
  rating: string;
  type: 'stable' | 'burst' | 'date' | 'study' | 'sports' | 'custom';
}

